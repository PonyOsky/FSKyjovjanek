Slovácký soubor Kyjov si v pátek 12. srpna na Letním kině v Kyjově od 20.30 hodin programem Krúžkaři a souboráci připomene 80. výročí svého založení. Chystaný pořad bude kromě oslavy také zajímavou sondou do historie chorografie SS Kyjov - na pódiu se vystřídají taneční čísla od 50. let minulého století až po výstupy z nedávné minulosti i ze současného repertoáru souborových tanečníků.<br>Pořad s názvem Krúžkaři a souboráci se bude konat na letním kině v Kyjově v pátek 12. srpna od 20.30. V autorském pořadu Ladislava Šimečka, tradičního skvělého moderátora soutěže o nejlepšího tanečníka slováckého verbuňku, se představí taneční čísla od 50. let minulého století až po chorografie současných tanečníků.<br>V pořadu vystoupí několik generací souborových tanečníků včetně tří vítězů soutěže o nejlepšího tanečníka slováckého verbuňku. Doprovodí je cimbálové muziky Širúch, Cimbálová muzika Jury Petrů a Cimbálová muzika Pavla Růžičky. Přijeďte příští pátek do Kyjova, budete vítáni.
