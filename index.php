            <p>Slovácký soubor Kyjov, z.s. působí v královském městě Kyjov, jednom z center národopisného dění jihovýchodní Moravy, srdci regionu kyjovské Dolňácko.</p>
            <p>Od založení v roce 1942 je prvořadým posláním souboru objevování starých a zapadlých zvyků, obyčejů, písní a tanců, ale také krojových součástí či hospodářského nářadí a současné udržování a rozvíjení <a class="hrefs" href="zvykoslovi.html">tradičního zvykosloví</a> v místě svého působení. Soubor v současné době tvoří několik samostatných složek: Taneční složku, která je nejstarší organizovanou <a class="hrefs" href="slozky.html">součástí souboru</a>, mužský sbor, ženský sbor Tragačnice a Pajtáše a cimbálovou muziku Jury Petrů.</p>
            <p>Zásluhou souboru se v Kyjově, městě s 13 000 obyvateli a rozvinutým průmyslem, dodnes živě, za bohaté účasti široké veřejnosti, udržují tradiční roční zvykoslovné pořady jako fašaňková a velikonoční obchůzka, stavění a kácení máje, dožínky, hody aj. Soubor je pořadatelem pravidelných besed u cimbálu a dalších pořadů, zaměřených na lidovou kulturu. Členové souboru patří k hlavním organizátorům a účinkujícím velkého folklórního festivalu „Slovácký rok“ v Kyjově, který je pořádán jako nejstarší v Česku již od roku 1921.</p>
